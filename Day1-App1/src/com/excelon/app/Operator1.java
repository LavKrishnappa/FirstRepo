package com.excelon.app;

public class Operator1 {
	
	public static void main(String[] args) {
		int num1=10,num2=3;
		System.out.println("num1+num2="+(num1+num2));
		System.out.println("num1-num2="+(num1-num2));
		System.out.println("num1%num2="+(num1%num2));
		System.out.println("num1/num2="+(num1/num2));
		System.out.println("num1*num2="+(num1*num2));
		
	}

}
